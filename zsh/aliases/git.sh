# Add
alias ga="git add"
alias gaa="git add ."

# Manage branch
alias gco="git checkout"
alias gcob="git checkout -b "
alias gbd="git branch -d "

# Commit
alias gcm="git commit -v -m "
alias gca="git commit -v --amend"

# Infos
