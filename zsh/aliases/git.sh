alias gs="git status"
alias gd="git diff"
alias gdc='git diff --compact-summary'
alias gdstat='git diff --numstat'

# Reset
alias grsh="git reset HARD"

# Sync
alias gpl="git pull"
alias gp="git push"
alias gpf="git push --force"
